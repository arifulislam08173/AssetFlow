    const rows: any[] = await Asset.findAll({ include: [{ model: Company, as: "company" }, { model: AssetCategory, as: "category" }, { model: Vendor, as: "vendor" }, { model: Location, as: "location" }, { model: Employee, as: "assignedEmployee" }], order: [["assetCode", "ASC"]] });
