import { Router } from "express";
import { Asset, AssetAssignment, AssetCategory, AssetReturn, AuditLog, Company, Employee, Location, Purchase, RepairTicket, Vendor } from "../models";

export const exportRoutes = Router();

function csvEscape(v: unknown) {
  return `"${String(v ?? "").replace(/"/g, '""')}"`;
}

function safe(v: unknown) {
  return String(v ?? "-").replace(/[&<>"']/g, (ch) => ({ "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;", "'": "&#039;" }[ch] || ch));
}

function reportHtml(title: string, columns: string[], rows: unknown[][], summary?: string) {
  const compact = columns.length > 8;
  return `<!doctype html><html><head><title>${safe(title)}</title><style>
  *{box-sizing:border-box}body{font-family:Arial,Helvetica,sans-serif;padding:28px;color:#0f172a;background:#fff}.brand{display:flex;align-items:flex-start;justify-content:space-between;border-bottom:3px solid #0f766e;padding-bottom:14px;margin-bottom:18px}.brand h1{margin:0;color:#0f766e;font-size:28px}.brand p{margin:4px 0 0;color:#64748b;font-size:12px}h2{margin:0 0 10px;font-size:20px}.summary{border:1px solid #cbd5e1;border-radius:14px;padding:12px 14px;margin:14px 0;background:#f8fafc;color:#334155;font-size:12px}table{width:100%;border-collapse:collapse;margin-top:12px}th{background:#f0fdfa;color:#115e59;font-weight:700}td,th{border:1px solid #cbd5e1;padding:${compact ? "6px" : "8px"};font-size:${compact ? "9.5px" : "11px"};text-align:left;vertical-align:top}tr:nth-child(even){background:#f8fafc}.footer{margin-top:18px;color:#64748b;font-size:11px}@page{size:A4 landscape;margin:12mm}@media print{button{display:none}body{padding:0}}
 </style></head><body><div class="brand"><div><h1>AssetFlow</h1><p>Enterprise Asset Management Suite</p></div><p>Generated ${new Date().toLocaleString()}</p></div><h2>${safe(title)}</h2>${summary ? `<div class="summary">${summary}</div>` : ""}<table><thead><tr>${columns.map((c) => `<th>${safe(c)}</th>`).join("")}</tr></thead><tbody>${rows.map((r) => `<tr>${r.map((c) => `<td>${safe(c)}</td>`).join("")}</tr>`).join("")}</tbody></table><div class="footer">This report is generated by AssetFlow. Use browser print/save as PDF for a final PDF copy.</div><script>window.print()</script></body></html>`;
}

function sendHtml(res: any, filename: string, html: string) {
  res.setHeader("Content-Type", "text/html; charset=utf-8");
  res.setHeader("Content-Disposition", `inline; filename=${filename}`);
  res.send(html);
}

exportRoutes.get("/assets.csv", async (_req, res, next) => {
  try {
    const rows = await Asset.findAll({ include: [{ model: Company, as: "company" }, { model: AssetCategory, as: "category" }, { model: Vendor, as: "vendor" }, { model: Location, as: "location" }, { model: Employee, as: "assignedEmployee" }], order: [["assetCode", "ASC"]] });
    const header = ["Company", "Asset Code", "Name", "Category", "Serial", "Status", "Condition", "Employee", "Location", "Purchase Price", "Current Value", "Useful Life", "Warranty Expiry"];
    const lines = [header.join(","), ...rows.map((a: any) => [a.company?.name, a.assetCode, a.name, a.category?.name, a.serialNumber, a.status, a.condition, a.assignedEmployee ? `${a.assignedEmployee.employeeCode} - ${a.assignedEmployee.name}` : "", a.location?.name, a.purchasePrice, a.currentValue, `${a.usefulLifeYears} years`, a.warrantyExpiry].map(csvEscape).join(","))];
    res.setHeader("Content-Type", "text/csv");
    res.setHeader("Content-Disposition", "attachment; filename=assetflow-assets.csv");
    res.send(lines.join("\n"));
  }
  catch (e) {
    next(e);
  }
});

