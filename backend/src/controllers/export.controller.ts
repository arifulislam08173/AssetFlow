exportRoutes.get("/finance.pdf", async (_req, res, next) => {
  try {
    const rows: any[] = await Asset.findAll({ include: [{ model: Company, as: "company" }, { model: AssetCategory, as: "category" }, { model: Employee, as: "assignedEmployee" }], order: [["purchaseDate", "ASC"]] });
    const totalPurchase = rows.reduce((s, a) => s + Number(a.purchasePrice || 0), 0);
    const totalCurrent = rows.reduce((s, a) => s + Number(a.currentValue || 0), 0);
    sendHtml(res, "assetflow-finance-report.html", reportHtml("Depreciation & Finance Report", ["Company", "Asset Code", "Asset", "Employee", "Purchase Date", "Purchase Value", "Current Value", "Depreciation", "Useful Life", "Method", "Warranty"], rows.map((a: any) => [a.company?.name, a.assetCode, a.name, a.assignedEmployee ? `${a.assignedEmployee.employeeCode} - ${a.assignedEmployee.name}` : "-", a.purchaseDate, a.purchasePrice, a.currentValue, Number(a.purchasePrice || 0) - Number(a.currentValue || 0), `${a.usefulLifeYears} years`, a.depreciationMethod, a.warrantyExpiry]), `Total purchase value: <b>${totalPurchase}</b> &nbsp; Current book value: <b>${totalCurrent}</b> &nbsp; Accumulated depreciation: <b>${totalPurchase - totalCurrent}</b>`));
  }
  catch (e) {
    next(e);
  }
});

